# ─────────────────────────────────────────────────────────────
# Anthropic クライアント (cache)
# ─────────────────────────────────────────────────────────────
@st.cache_resource
def get_client():
    api_key = (
        st.secrets.get("ANTHROPIC_API_KEY", None)
        if hasattr(st, "secrets")
        else None
    ) or os.getenv("ANTHROPIC_API_KEY")
    if not api_key:
        return None
    try:
        from anthropic import Anthropic
        return Anthropic(api_key=api_key)
    except Exception:
        return None


CLIENT = get_client()
MODEL_ID = os.getenv("SUPPORT_MODEL", "claude-haiku-4-5-20251001")

# ─────────────────────────────────────────────────────────────
# System Prompt
#   - PR トピック (回答 OK)
#   - 内部技術 (回答 NG)
